<script lang="ts">
  import { createEventDispatcher } from 'svelte'
  import type { LayerSpecification } from 'maplibre-gl'
  import { LayerTypes } from '$lib/constants'
  import type { Layer } from '$lib/types'
  import { map } from '$stores'

  export let layer: Layer

  const dispatch = createEventDispatcher()
  const layerId = layer.id
  const propertyName = 'icon-keep-upright'
  const propertyNameSymbolPlacement = 'symbol-placement'
  const style = $map.getStyle().layers.filter((layer: LayerSpecification) => layer.id === layerId)[0]
  let parentType:
    | 'symbol'
    | 'fill'
    | 'raster'
    | 'line'
    | 'circle'
    | 'heatmap'
    | 'fill-extrusion'
    | 'hillshade'
    | 'background' = LayerTypes.SYMBOL

  const parentId = layer.parentId
  if (parentId) {
    const parentStyle = $map.getStyle().layers.filter((layer: LayerSpecification) => layer.id === parentId)[0]
    parentType = parentStyle.type
  }

  // let checked = style.layout && style.layout[propertyName] ? style.layout[propertyName] : false
  let choices = ['point', 'line', 'line-center']

  let defaultValue = 'point'
  switch (parentType) {
    case LayerTypes.LINE:
      defaultValue = 'line-center'
      break
    case LayerTypes.FILL:
      defaultValue = 'point'
      break
  }

  let selected =
    style.layout && style.layout[propertyNameSymbolPlacement] ? style.layout[propertyNameSymbolPlacement] : defaultValue

  $: selected, setSymbolPlacement()

  const setSymbolPlacement = () => {
    if (style.type !== LayerTypes.SYMBOL) return
    const newStyle = JSON.parse(JSON.stringify(style))
    if (!newStyle.layout) {
      newStyle.layout = {}
    }

    newStyle.layout[propertyNameSymbolPlacement] = selected
    $map.setLayoutProperty(layerId, propertyNameSymbolPlacement, selected)
    $map.setLayoutProperty(layerId, propertyName, selected !== 'point')

    dispatch('change')
  }
</script>

<div
  class="select"
  style="height: 30px;">
  <select
    bind:value={selected}
    style="width: 100%;"
    alt="text-field"
    title="Icon overlap">
    {#each choices as choice}
      <option
        class="legend-text"
        value={choice}>{choice}</option>
    {/each}
  </select>
</div>
